function onHover(name, num) {
  var img = document.getElementById(name + "" + num);
  img.src = "Images/Skill Tree/Dribbling/" + name + " On Hover.png";
}

function offHover(name, num) {
  var img = document.getElementById(name + "" + num);
  img.src = "Images/Skill Tree/Dribbling/" + name + ".png";
}
